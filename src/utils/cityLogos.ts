/**
 * 获取城市标志 URL
 * @param cityName 城市名称
 * @param size 图片尺寸（默认 200px）
 * @returns 城市标志 URL，如果没有找到则返回 null
 */
export function getCityLogo(cityName: string, size: number = 200): string | null {
  if (!cityName) return null
  
  // 直接匹配
  if (cityLogos[cityName]) {
    return cityLogos[cityName].replace('/200px-', `/${size}px-`)
  }
  
  // 去除后缀匹配（如"北京市" -> "北京"）
  const cleanName = cityName.replace(/市 | 自治州 | 地区 | 县/g, '')
  if (cityLogos[cleanName]) {
    return cityLogos[cleanName].replace('/200px-', `/${size}px-`)
  }
  
  return null
}

/**
 * 获取所有可用城市标志
 * @returns 城市名称数组
 */
export function getAvailableCities(): string[] {
  return Object.keys(cityLogos)
}

/**
 * 检查城市是否有标志
 * @param cityName 城市名称
 * @returns 是否有标志
 */
export function hasCityLogo(cityName: string): boolean {
  return getCityLogo(cityName) !== null
}
